<script>
import { mapGetters } from "vuex";
export default {
  name: "ShopCartIndex",

  data() {
    return {
      timer: null,
    };
  },

  computed: {
    ...mapGetters(["shopCartList"]),

    sumPrice() {
      let sum = 0;
      this.shopCartList.forEach((elem) => {
        sum += elem.skuPrice * elem.skuNum;
      });
      return sum;
    },

    isAllChecked() {
      return (
        this.shopCartList.length != 0 &&
        this.shopCartList.every((item) => item.isChecked == 1)
      );
    },
  },

  mounted() {
    this.getShoppcartList();
  },

  methods: {
    getShoppcartList() {
      this.$store.dispatch("getShopcartList");
    },

    async handler(num, type, item) {
      if (this.timer) return;
      this.timer = setTimeout(() => {
        this.timer = null;
      }, 500);
      switch (type) {
        case "plus":
          num = item.skuNum > 98 ? 0 : 1;
          break;
        case "mins":
          num = item.skuNum > 1 ? -1 : 0;
          break;
        case "multi":
          // 表单验证
          num = isNaN(num) || num < 1 ? 0 : parseInt(num);
          num = num > 99 ? 99 - item.skuNum : num - item.skuNum;
          break;
      }
      try {
        await this.$store.dispatch("addShopCart", {
          skuid: item.skuId,
          skuNum: num,
        });
        this.getShoppcartList();
      } catch (error) {
        alert(error);
      }
    },

    async deleteCartList(skuId) {
      try {
        await this.$store.dispatch("deleteCartList", skuId);
        this.getShoppcartList();
      } catch (e) {
        alert(e.message);
      }
    },

    async updateChecked(skuId, checked) {
      try {
        checked = checked === true ? 1 : 0;
        await this.$store.dispatch("updateChecked", { skuId, checked });
        this.getShoppcartList();
      } catch (e) {
        alert(e);
      }
    },

    async deleteAllChecked() {
      try {
        await this.$store.dispatch("deleteAllChecked");
        this.getShoppcartList();
      } catch (err) {
        alert(err);
      }
    },

    async updateAllChecked(cur) {
      try {
        await this.$store.dispatch("updateAllChecked", cur);
        this.getShoppcartList();
      } catch (error) {
        alert(error);
      }
    },
  },
};
</script>

